package com.example.drivertani;


public class Route {
    String From,To,Time,Price,Ride_ID,Driver_Mail,Status;
    String Date;



    public String getFrom() {
        return From;
    }

    public String getTo() {
        return To;
    }

    public String getTime() {
        return Time;
    }

    public String getPrice() {
        return Price;
    }


    public String getDate() {
        return Date;
    }

    public String getRide_ID() {
        return Ride_ID;
    }

    public String getDriver_Mail() {
        return Driver_Mail;
    }

    public String getStatus() {
        return Status;
    }
}

